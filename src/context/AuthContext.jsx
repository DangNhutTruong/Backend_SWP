import React, { createContext, useState, useContext, useEffect } from 'react';

// Base API URL - change this to your backend URL
const API_BASE_URL = 'http://localhost:5000/api';

// T·∫°o context cho x√°c th·ª±c
const AuthContext = createContext(null);

// Hook t√πy ch·ªânh ƒë·ªÉ s·ª≠ d·ª•ng AuthContext
export const useAuth = () => useContext(AuthContext);

// Provider component
export const AuthProvider = ({ children }) => {
  // Kh·ªüi t·∫°o tr·∫°ng th√°i t·ª´ sessionStorage (gi·ªØ khi reload, m·∫•t khi ƒë√≥ng browser)
  const [user, setUser] = useState(() => {
    const storedUser = sessionStorage.getItem('nosmoke_user') || localStorage.getItem('nosmoke_user');
    return storedUser ? JSON.parse(storedUser) : null;
  });
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);
  const [token, setToken] = useState(() => {
    return sessionStorage.getItem('nosmoke_token') || localStorage.getItem('nosmoke_token');
  });
  const [refreshToken, setRefreshToken] = useState(() => {
    return localStorage.getItem('nosmoke_refresh_token');
  });
  const [rememberMe, setRememberMe] = useState(() => {
    return localStorage.getItem('nosmoke_remember') === 'true';
  });
  // X√≥a localStorage c≈© v√† sync v·ªõi sessionStorage
  useEffect(() => {
    // Kh√¥ng x√≥a localStorage n·ªØa v√¨ c·∫ßn cho Remember Me
    console.log('üîÑ AuthContext initialized');
  }, []);

  // L∆∞u user v√† token v√†o storage khi thay ƒë·ªïi
  useEffect(() => {
    if (user) {
      if (rememberMe) {
        localStorage.setItem('nosmoke_user', JSON.stringify(user));
      } else {
        sessionStorage.setItem('nosmoke_user', JSON.stringify(user));
        localStorage.removeItem('nosmoke_user');
      }
    } else {
      sessionStorage.removeItem('nosmoke_user');
      localStorage.removeItem('nosmoke_user');
    }
  }, [user, rememberMe]);

  useEffect(() => {
    if (token) {
      if (rememberMe) {
        localStorage.setItem('nosmoke_token', token);
      } else {
        sessionStorage.setItem('nosmoke_token', token);
        localStorage.removeItem('nosmoke_token');
      }
    } else {
      sessionStorage.removeItem('nosmoke_token');
      localStorage.removeItem('nosmoke_token');
    }
  }, [token, rememberMe]);

  useEffect(() => {
    if (refreshToken && rememberMe) {
      localStorage.setItem('nosmoke_refresh_token', refreshToken);
    } else {
      localStorage.removeItem('nosmoke_refresh_token');
    }
  }, [refreshToken, rememberMe]);

  useEffect(() => {
    localStorage.setItem('nosmoke_remember', rememberMe.toString());
  }, [rememberMe]);
  // API helper function
  const apiCall = async (endpoint, options = {}) => {
    try {
      const config = {
        headers: {
          'Content-Type': 'application/json',
          ...options.headers
        },
        ...options
      };

      // Add authorization header if token exists
      if (token) {
        config.headers.Authorization = `Bearer ${token}`;
      }

      const url = `${API_BASE_URL}${endpoint}`;
      console.log('üåê Fetching:', url, 'with config:', config); const response = await fetch(url, config);

      console.log('üì° Response status:', response.status);
      console.log('üì° Response ok:', response.ok);

      // X·ª≠ l√Ω tr∆∞·ªùng h·ª£p server error (500)
      if (response.status === 500) {
        console.error('‚ùå Server error (500)');
        throw new Error('L·ªói m√°y ch·ªß n·ªôi b·ªô. Vui l√≤ng th·ª≠ l·∫°i sau.');
      }

      const data = await response.json();
      console.log('üì° Response data:', data);

      if (!response.ok) {
        console.error('‚ùå API Error:', data);
        throw new Error(data.message || 'Y√™u c·∫ßu th·∫•t b·∫°i');
      }

      return data;
    } catch (error) {
      // Log chi ti·∫øt l·ªói bao g·ªìm stack trace
      console.error('üö® API call error details:', error);
      console.error('üö® apiCall error:', error);
      throw error;
    }
  };  // H√†m ƒëƒÉng k√Ω t√†i kho·∫£n m·ªõi - B∆∞·ªõc 1: G·ª≠i m√£ x√°c nh·∫≠n
  const register = async (userData) => {
    setLoading(true);
    setError(null);

    try {
      console.log('üöÄ AuthContext register called with:', userData);
      console.log('üì° Making API call to:', `${API_BASE_URL}/auth/register`);

      const data = await apiCall('/auth/register', {
        method: 'POST',
        body: JSON.stringify(userData)
      });

      console.log('‚úÖ API response:', data);

      if (data.success) {
        // Registration successful, verification code sent
        return {
          success: true,
          message: data.message,
          email: data.data.email,
          needsVerification: true
        };
      } else {
        throw new Error(data.message);
      }
    } catch (err) {
      setError(err.message);
      return { success: false, error: err.message };
    } finally {
      setLoading(false);
    }
  };  // H√†m x√°c nh·∫≠n email
  const verifyEmail = async (email, verificationCode) => {
    setLoading(true);
    setError(null);

    try {
      console.log(`üîç X√°c th·ª±c email: ${email} v·ªõi m√£: ${verificationCode}`);
      console.log(`üåê API URL: ${API_BASE_URL}/auth/verify-email`);

      // ƒê·∫£m b·∫£o m√£ x√°c th·ª±c lu√¥n l√† string v√† lo·∫°i b·ªè kho·∫£ng tr·∫Øng
      const formattedCode = String(verificationCode).trim();

      console.log(`üì§ G·ª≠i request v·ªõi d·ªØ li·ªáu:`, { email, verificationCode: formattedCode });

      const data = await apiCall('/auth/verify-email', {
        method: 'POST',
        body: JSON.stringify({
          email,
          verificationCode: formattedCode
        })
      });

      console.log(`üì• Nh·∫≠n response:`, data); if (data.success) {
        setUser(data.data.user);
        setToken(data.data.token);
        console.log('‚úÖ Email verified and user registered');
        return { success: true, user: data.data.user };
      } else {
        console.error('‚ùå Verification failed:', data.message);
        throw new Error(data.message || 'X√°c th·ª±c email kh√¥ng th√†nh c√¥ng');
      }
    } catch (err) {
      console.error('üî¥ L·ªói khi x√°c th·ª±c email:', err);
      console.error('‚ùå Verification error:', err);
      setError(err.message || 'Kh√¥ng th·ªÉ k·∫øt n·ªëi ƒë·∫øn server');
      return { success: false, error: err.message || 'Kh√¥ng th·ªÉ k·∫øt n·ªëi ƒë·∫øn server' };
    } finally {
      setLoading(false);
    }
  };

  // H√†m g·ª≠i l·∫°i m√£ x√°c nh·∫≠n
  const resendVerificationCode = async (email) => {
    setLoading(true);
    setError(null);

    try {
      const data = await apiCall('/auth/resend-verification', {
        method: 'POST',
        body: JSON.stringify({ email })
      });

      if (data.success) {
        return { success: true, message: data.message };
      } else {
        throw new Error(data.message);
      }
    } catch (err) {
      setError(err.message);
      return { success: false, error: err.message };
    } finally {
      setLoading(false);
    }
  };
  // H√†m ƒëƒÉng nh·∫≠p
  const login = async (email, password, remember = false) => {
    setLoading(true);
    setError(null);

    try {
      const data = await apiCall('/auth/login', {
        method: 'POST',
        body: JSON.stringify({ email, password })
      }); if (data.success) {
        setRememberMe(remember);
        setUser(data.data.user);
        setToken(data.data.token);
        
        // L∆∞u refresh token n·∫øu c√≥
        if (data.data.refreshToken) {
          setRefreshToken(data.data.refreshToken);
        }
        
        console.log(`‚úÖ User logged in${remember ? ' with remember me' : ' - session only'}`);
        return { success: true, user: data.data.user };
      } else {
        throw new Error(data.message);
      }
    } catch (err) {
      setError(err.message);
      return { success: false, error: err.message };
    } finally {
      setLoading(false);
    }
  };
  // H√†m ƒëƒÉng xu·∫•t
  const logout = async () => {
    try {
      if (token) {
        await apiCall('/auth/logout', {
          method: 'POST'
        });
      }
    } catch (err) {
      console.error('Logout error:', err);    } finally {
      // X√≥a t·∫•t c·∫£ d·ªØ li·ªáu ƒëƒÉng nh·∫≠p
      setUser(null);
      setToken(null);
      setRefreshToken(null);
      setRememberMe(false);
      
      // X√≥a kh·ªèi storage
      sessionStorage.removeItem('nosmoke_user');
      sessionStorage.removeItem('nosmoke_token');
      localStorage.removeItem('nosmoke_user');
      localStorage.removeItem('nosmoke_token');
      localStorage.removeItem('nosmoke_refresh_token');
      localStorage.removeItem('nosmoke_remember');
      
      console.log('üîê User logged out - all session data cleared');
      return { success: true };
    }
  };

  // H√†m c·∫≠p nh·∫≠t th√¥ng tin ng∆∞·ªùi d√πng
  const updateUser = async (updatedData) => {
    if (!user || !token) return { success: false, error: 'Kh√¥ng c√≥ ng∆∞·ªùi d√πng ƒë·ªÉ c·∫≠p nh·∫≠t' };

    try {
      setLoading(true);
      const data = await apiCall('/auth/profile', {
        method: 'PUT',
        body: JSON.stringify(updatedData)
      });

      if (data.success) {
        setUser(data.data.user);
        return { success: true, user: data.data.user };
      } else {
        throw new Error(data.message);
      }
    } catch (err) {
      setError(err.message);
      return { success: false, error: err.message };
    } finally {
      setLoading(false);
    }
  };

  // H√†m ƒë·ªïi m·∫≠t kh·∫©u
  const changePassword = async (currentPassword, newPassword, confirmPassword) => {
    if (!user || !token) return { success: false, error: 'Kh√¥ng c√≥ ng∆∞·ªùi d√πng ƒë·ªÉ c·∫≠p nh·∫≠t' };

    try {
      setLoading(true);
      const data = await apiCall('/auth/change-password', {
        method: 'POST',
        body: JSON.stringify({ currentPassword, newPassword, confirmPassword })
      });

      if (data.success) {
        return { success: true };
      } else {
        throw new Error(data.message);
      }
    } catch (err) {
      setError(err.message);
      return { success: false, error: err.message };
    } finally {
      setLoading(false);
    }
  };

  // H√†m refresh th√¥ng tin user t·ª´ server
  const refreshUser = async () => {
    if (!token) return { success: false, error: 'Kh√¥ng c√≥ token' };

    try {
      const data = await apiCall('/auth/profile');

      if (data.success) {
        setUser(data.data.user);
        return { success: true, user: data.data.user };
      } else {
        throw new Error(data.message);
      }
    } catch (err) {
      // If token is invalid, logout
      if (err.message.includes('token') || err.message.includes('401')) {
        logout();
      }
      return { success: false, error: err.message };
    }
  };
  // Legacy functions for backward compatibility
  const refreshMembership = refreshUser;

  // H√†m refresh token t·ª± ƒë·ªông
  const refreshAccessToken = async () => {
    if (!refreshToken) {
      console.log('‚ùå No refresh token available');
      return false;
    }

    try {
      console.log('üîÑ Refreshing access token...');
      const data = await fetch(`${API_BASE_URL}/auth/refresh-token`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ refreshToken })
      });

      const result = await data.json();

      if (result.success) {
        setToken(result.data.token);
        if (result.data.refreshToken) {
          setRefreshToken(result.data.refreshToken);
        }
        console.log('‚úÖ Access token refreshed successfully');
        return true;
      } else {
        console.log('‚ùå Failed to refresh token:', result.message);
        logout(); // Logout if refresh fails
        return false;
      }
    } catch (error) {
      console.error('‚ùå Refresh token error:', error);
      logout(); // Logout if refresh fails
      return false;
    }
  };

  // Auto refresh token khi kh·ªüi ƒë·ªông app (n·∫øu c√≥ remember me)
  useEffect(() => {
    const initializeAuth = async () => {
      if (refreshToken && rememberMe && !token) {
        console.log('üîÑ Auto refreshing token on app startup...');
        await refreshAccessToken();
      }
    };

    initializeAuth();
  }, []); // Ch·ªâ ch·∫°y m·ªôt l·∫ßn khi component mount  // Gi√° tr·ªã context
  const value = {
    user,
    loading,
    error,
    token,
    refreshToken,
    rememberMe,
    login,
    logout,
    register,
    verifyEmail,
    resendVerificationCode,
    updateUser,
    changePassword,
    refreshUser,
    refreshMembership,
    refreshAccessToken,
    setUser,
    setRememberMe,
    isAuthenticated: !!user && !!token
  };

  return <AuthContext.Provider value={value}>{children}</AuthContext.Provider>;
};

export default AuthContext;
